package com.pramod;

public class ForLoop {
    public static void main(String[] args){


        for(int i=0; i<10; i++)
            System.out.println(i);


        //For-Each loop is used to loop through elements in an "Array"

        String cars[] = {"Bmw","Audi", "Tata"};
        for(String i:cars){
            System.out.println(i);
        }


}}
